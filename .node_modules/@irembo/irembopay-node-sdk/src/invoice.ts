 import { Base } from "./base";
export class Invoice extends Base {
  async createInvoice(data: CreateInvoiceInterface): Promise<any> {
    try {
      const response = await this.axiosInstance.post("/invoices", data);
      return response.data;
    } catch (error) {
      this.handleError(error);
    }
  }
  async createBatchInvoice(data: CreateBatchInvoiceInterface): Promise<any> {
    try {
      const response = await this.axiosInstance.post("/invoices/batch", data);
      return response.data;
    } catch (error) {
      this.handleError(error);
    }
  }
  async getInvoice(invoiceReference: number): Promise<any> {
    try {
      const response = await this.axiosInstance.get(`/invoices/${invoiceReference}`);
      return response.data;
    } catch (error) {
      this.handleError(error);
    }
  }
  async updateInvoice(invoiceReference: number, data: UpdateInvoiceInterface): Promise<any> {
    try {
      const response = await this.axiosInstance.put(`/invoices/${invoiceReference}`, data);
      return response.data;
    } catch (error) {
      this.handleError(error);
    }
  }
}
